/*18. Write a program to find minimum and maximum number as well as 
to add two integer numbers using methods of Integer. */

class Ass_Q18
{
  public static void main(String args[])
  {
     int num1=25;
     int num2=288;

     System.out.println("maximum number = "+Integer.max(num1,num2));
     System.out.println("minimum number = "+Integer.min(num1,num2));
     System.out.println("Addition number = "+Integer.sum(num1,num2));


    
  }

}