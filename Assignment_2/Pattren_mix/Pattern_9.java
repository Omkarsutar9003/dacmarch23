class Pattern_9
{
   public static void main(String args[])
   { 


   
         for(char i=65;i<=69;i++)
		 {
             for(char k=69;k>=i;k--)
			 {
				 System.out.print(" ");
				 
			 }
			 
			 for(char j=65;j<=i;j++)
			 {
				// System.out.print(" ");  
				 System.out.print(j+" ");
				
			 }
             System.out.println();

		 }			 
		 
		 }
 }